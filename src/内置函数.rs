/// 内置函数 —— 诚言编程语言

use crate::值类型::*;
use std::collections::HashMap as 哈希映射;

pub fn 注册内置函数(全局变量: &mut 哈希映射<String, 值>) {
    全局变量.insert("打印".into(), 值::原生函数(原生函数对象 { 名称: "打印".into(), 函数: 内置打印 }));
    全局变量.insert("文本化".into(), 值::原生函数(原生函数对象 { 名称: "文本化".into(), 函数: 内置文本化 }));
    全局变量.insert("输入".into(), 值::原生函数(原生函数对象 { 名称: "输入".into(), 函数: 内置输入 }));
    全局变量.insert("长度".into(), 值::原生函数(原生函数对象 { 名称: "长度".into(), 函数: 内置长度 }));
    全局变量.insert("绝对值".into(), 值::原生函数(原生函数对象 { 名称: "绝对值".into(), 函数: 内置绝对值 }));
    全局变量.insert("最大值".into(), 值::原生函数(原生函数对象 { 名称: "最大值".into(), 函数: 内置最大值 }));
    全局变量.insert("最小值".into(), 值::原生函数(原生函数对象 { 名称: "最小值".into(), 函数: 内置最小值 }));
    全局变量.insert("类型".into(), 值::原生函数(原生函数对象 { 名称: "类型".into(), 函数: 内置类型 }));
    全局变量.insert("断言".into(), 值::原生函数(原生函数对象 { 名称: "断言".into(), 函数: 内置断言 }));
    全局变量.insert("追加".into(), 值::原生函数(原生函数对象 { 名称: "追加".into(), 函数: 内置追加 }));
    全局变量.insert("圆周率".into(), 值::小数(std::f64::consts::PI));
    全局变量.insert("自然常数".into(), 值::小数(std::f64::consts::E));
}

fn 内置打印(参数: &[值]) -> 值 {
    let 输出: Vec<String> = 参数.iter().map(|v| v.转文本()).collect();
    打印行!("{}", 输出.join(" "));
    值::空值
}

fn 内置文本化(参数: &[值]) -> 值 {
    if 参数.is_empty() { return 值::文本(String::new()); }
    值::文本(参数[0].转文本())
}

fn 内置输入(参数: &[值]) -> 值 {
    if !参数.is_empty() {
        打印!("{}", 参数[0].转文本());
        use std::io::Write;
        let _ = std::io::stdout().flush();
    }
    let mut 输入 = String::new();
    if std::io::stdin().read_line(&mut 输入).is_err() { return 值::空值; }
    值::文本(输入.trim().to_string())
}

fn 内置长度(参数: &[值]) -> 值 {
    if 参数.is_empty() { return 值::空值; }
    match &参数[0] {
        值::数组(a) => 值::整数(a.len() as i64),
        值::映射(m) => 值::整数(m.len() as i64),
        值::文本(s) => 值::整数(s.chars().count() as i64),
        _ => 值::空值,
    }
}

fn 内置绝对值(参数: &[值]) -> 值 {
    if 参数.is_empty() { return 值::空值; }
    match &参数[0] {
        值::整数(n) => n.checked_abs().map(值::整数).unwrap_or(值::空值),
        值::小数(n) => 值::小数(n.abs()),
        _ => 值::空值,
    }
}

fn 内置最大值(参数: &[值]) -> 值 {
    if 参数.len() < 2 { return 值::空值; }
    match (&参数[0], &参数[1]) {
        (值::整数(a), 值::整数(b)) => 值::整数(*a.max(b)),
        (值::小数(a), 值::小数(b)) => 值::小数(a.max(*b)),
        _ => 值::空值,
    }
}

fn 内置最小值(参数: &[值]) -> 值 {
    if 参数.len() < 2 { return 值::空值; }
    match (&参数[0], &参数[1]) {
        (值::整数(a), 值::整数(b)) => 值::整数(*a.min(b)),
        (值::小数(a), 值::小数(b)) => 值::小数(a.min(*b)),
        _ => 值::空值,
    }
}

fn 内置类型(参数: &[值]) -> 值 {
    if 参数.is_empty() { return 值::空值; }
    值::文本(参数[0].类型名().to_string())
}

fn 内置断言(参数: &[值]) -> 值 {
    if 参数.is_empty() { return 值::文本("断言失败: 没有提供条件".into()); }
    if !参数[0].转布尔() {
        let 消息 = if 参数.len() > 1 { 参数[1].转文本() } else { "断言失败".into() };
        return 值::文本(消息);
    }
    值::空值
}

fn 内置追加(参数: &[值]) -> 值 {
    if 参数.len() < 2 { return 值::空值; }
    match &参数[0] {
        值::数组(a) => {
            let mut 新数组 = a.clone();
            新数组.push(参数[1].clone());
            值::数组(新数组)
        }
        _ => 值::空值,
    }
}
