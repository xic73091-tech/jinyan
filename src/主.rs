use std::env;
use std::fs;
use std::process;

fn main() {
    let 参数: Vec<String> = env::args().collect();

    if 参数.len() < 2 {
        打印行!("╔══════════════════════════════════╗");
        打印行!("║      诚言编程语言 v0.1.0         ║");
        打印行!("║      全中文编程语言              ║");
        打印行!("╚══════════════════════════════════╝");
        打印行!();
        打印行!("用法:");
        打印行!("  诚言 运行 <文件.jy>    运行源文件");
        打印行!("  诚言 交互              进入交互模式");
        打印行!("  诚言 帮助              显示帮助信息");
        process::exit(0);
    }

    match 参数[1].as_str() {
        "运行" => {
            if 参数.len() < 3 {
                错误输出!("错误: 缺少源文件路径");
                错误输出!("用法: 诚言 运行 <文件.jy>");
                process::exit(1);
            }
            运行文件(&参数[2]);
        }
        "交互" => 交互模式(),
        "帮助" => 显示帮助(),
        文件路径 => 运行文件(文件路径),
    }
}

fn 运行文件(路径: &str) {
    let 源码 = match fs::read_to_string(路径) {
        Ok(内容) => 内容,
        Err(错误) => {
            错误输出!("无法读取文件 '{}': {}", 路径, 错误);
            process::exit(1);
        }
    };

    if let Err(错误) = 执行源码(&源码, 路径) {
        错误输出!("{}", 错误);
        process::exit(1);
    }
}

fn 执行源码(源码: &str, 文件名: &str) -> Result<(), String> {
    // 阶段一：词法分析
    let 记号列表 = 词法分析器::词法分析器::分析(源码, 文件名)
        .map_err(|错误| 格式化!("词法错误: {}", 错误))?;

    // 阶段二：语法分析
    let 程序 = 语法分析器::语法分析器::解析(&记号列表)
        .map_err(|错误| 格式化!("语法错误: {}", 错误))?;

    // 阶段三：编译为字节码
    let 字节码 = 编译器::编译器::编译(&程序)
        .map_err(|错误| 格式化!("编译错误: {}", 错误))?;

    // 阶段四：虚拟机执行
    let mut 虚拟机 = 虚拟机::虚拟机::新建();
    虚拟机.执行(&字节码)
        .map_err(|错误| 格式化!("运行时错误: {}", 错误))?;

    Ok(())
}

fn 交互模式() {
    打印行!("╔══════════════════════════════════╗");
    打印行!("║      诚言交互模式 v0.1.0         ║");
    打印行!("║      输入 '退出' 结束            ║");
    打印行!("╚══════════════════════════════════╝");

    loop {
        打印!("诚言> ");
        use std::io::Write;
        let _ = std::io::stdout().flush();

        let mut 输入 = String::new();
        match std::io::stdin().read_line(&mut 输入) {
            Ok(0) => { 打印行!(); break; }
            Ok(_) => {}
            Err(_) => { continue; }
        }

        let 输入 = 输入.trim();
        if 输入 == "退出" || 输入 == "exit" { 打印行!("再见！"); break; }
        if 输入.is_empty() { continue; }

        if let Err(错误) = 执行源码(输入, "<交互>") {
            错误输出!("{}", 错误);
        }
    }
}

