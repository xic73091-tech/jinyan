/// 字节码编译器 —— 诚言编程语言

use crate::抽象语法树::*;
use crate::字节码::*;
use crate::错误::编译错误;
use std::collections::HashMap as 哈希映射;

#[derive(Debug, Clone)]
struct 局部变量 { 名称: String, 槽位: u8 }

struct 作用域 { 局部变量: Vec<局部变量>, 深度: usize }

struct 循环上下文 { break跳转列表: Vec<usize>, continue跳转列表: Vec<usize>, continue目标: usize }

pub struct 编译器 {
    代码块: 字节码块,
    作用域栈: Vec<作用域>,
    当前深度: usize,
    当前行号: usize,
    全局名称: 哈希映射<String, u16>,
    循环栈: Vec<循环上下文>,
    在方法中: bool,
    遍历深度: usize,
}

impl 编译器 {
    pub fn 新建() -> Self {
        编译器 {
            代码块: 字节码块::新建(),
            作用域栈: vec![作用域 { 局部变量: Vec::new(), 深度: 0 }],
            当前深度: 0, 当前行号: 1,
            全局名称: 哈希映射::new(),
            循环栈: Vec::new(),
            在方法中: false,
            遍历深度: 0,
        }
    }

    pub fn 编译(程序: &程序) -> Result<字节码块, 编译错误> {
        let mut 编译器 = 编译器::新建();
        for 语句 in &程序.语句列表 { 编译器.编译语句(语句)?; }
        编译器.代码块.写入操作码(操作码::停止, 编译器.当前行号);
        Ok(编译器.代码块)
    }

    fn 编译语句(&mut self, 语句: &语句) -> Result<(), 编译错误> {
        match 语句 {
            语句::表达式语句(表达式) => { self.编译表达式(表达式)?; self.代码块.写入操作码(操作码::弹出, self.当前行号); }
            语句::变量声明 { 名称, 初始值, .. } => {
                if let Some(初始) = 初始值 { self.编译表达式(初始)?; } else { self.代码块.写入操作码(操作码::压入空, self.当前行号); }
                let 槽位 = self.声明局部变量(名称);
                self.代码块.写入操作码(操作码::设置局部, self.当前行号);
                self.代码块.写入字节(槽位);
            }
            语句::赋值 { 目标, 值 } => {
                match 目标 {
                    表达式::标识符(名称) => {
                        self.编译表达式(值)?;
                        if let Some(槽位) = self.查找局部变量(名称) {
                            self.代码块.写入操作码(操作码::设置局部, self.当前行号);
                            self.代码块.写入字节(槽位);
                        } else {
                            let 索引 = self.获取全局索引(名称);
                            self.代码块.写入操作码(操作码::设置全局, self.当前行号);
                            self.代码块.写入u16(索引);
                        }
                    }
                    表达式::索引访问 { 对象, 索引 } => {
                        self.编译表达式(对象)?;
                        self.编译表达式(索引)?;
                        self.编译表达式(值)?;
                        self.代码块.写入操作码(操作码::索引设置, self.当前行号);
                        // 索引设置弹出对象和索引，压入新数组/映射。需要存回原变量
                        if let 表达式::标识符(名称) = &**对象 {
                            if let Some(槽位) = self.查找局部变量(名称) {
                                self.代码块.写入操作码(操作码::设置局部, self.当前行号);
                                self.代码块.写入字节(槽位);
                            }
                        }
                    }
                    表达式::成员访问 { 对象, 成员 } => {
                        self.编译表达式(对象)?;
                        self.编译表达式(值)?;
                        let i = self.代码块.添加常量(常量::文本(成员.clone()));
                        self.代码块.写入操作码(操作码::属性设置, self.当前行号);
                        self.代码块.写入u16(i);
                        // 属性设置弹出对象，压入新对象。需要存回原变量
                        match &**对象 {
                            表达式::标识符(名称) => {
                                if let Some(槽位) = self.查找局部变量(名称) {
                                    self.代码块.写入操作码(操作码::设置局部, self.当前行号);
                                    self.代码块.写入字节(槽位);
                                }
                            }
                            表达式::本引用 => {
                                let 槽位 = if self.在方法中 { 1 } else { 0 };
                                self.代码块.写入操作码(操作码::设置局部, self.当前行号);
                                self.代码块.写入字节(槽位);
                            }
                            _ => {}
                        }
                    }
                    _ => {}
                }
            }
            语句::如果 { 条件, 真分支, 假分支 } => {
                self.编译表达式(条件)?;
                let 跳转假 = self.代码块.当前地址();
                self.代码块.写入操作码(操作码::假性跳转, self.当前行号);
                self.代码块.写入u16(0);
                for s in 真分支 { self.编译语句(s)?; }
                if let Some(假) = 假分支 {
                    let 跳转结束 = self.代码块.当前地址();
                    self.代码块.写入操作码(操作码::无条件跳转, self.当前行号);
                    self.代码块.写入u16(0);
                    let 假开始 = self.代码块.当前地址();
                    self.代码块.回填跳转(跳转假 + 1, 假开始);
                    for s in 假 { self.编译语句(s)?; }
                    let 结束 = self.代码块.当前地址();
                    self.代码块.回填跳转(跳转结束 + 1, 结束);
                } else {
                    let 结束 = self.代码块.当前地址();
                    self.代码块.回填跳转(跳转假 + 1, 结束);
                }
            }
            语句::当循环 { 条件, 体 } => {
                let 循环开始 = self.代码块.当前地址();
                self.循环栈.push(循环上下文 { break跳转列表: Vec::new(), continue跳转列表: Vec::new(), continue目标: 循环开始 });
                self.编译表达式(条件)?;
                let 跳转结束 = self.代码块.当前地址();
                self.代码块.写入操作码(操作码::假性跳转, self.当前行号);
                self.代码块.写入u16(0);
                for s in 体 { self.编译语句(s)?; }
                let 跳转地址 = self.代码块.当前地址();
                self.代码块.写入操作码(操作码::无条件跳转, self.当前行号);
                let 偏移 = (循环开始 as i64 - 跳转地址 as i64 - 3) as u16;
                self.代码块.写入u16(偏移);
                let 结束 = self.代码块.当前地址();
                self.代码块.回填跳转(跳转结束 + 1, 结束);
                let 循环上下文 = self.循环栈.pop().unwrap();
                for 地址 in 循环上下文.break跳转列表 { self.代码块.回填跳转(地址, 结束); }
                for 地址 in 循环上下文.continue跳转列表 { self.代码块.回填跳转(地址, 循环上下文.continue目标); }
            }
            语句::遍历 { 变量名, 集合, 体 } => {
                // 遍历编译为：let _集合 = 集合; let _索引 = 0; while _索引 < 长度(_集合) { let 变量名 = _集合[_索引]; ...; _索引 = _索引 + 1; }
                // 用遍历深度生成唯一临时变量名，防止嵌套遍历冲突
                let 深度 = self.遍历深度;
                self.遍历深度 += 1;
                let 集合名 = 格式化!("_遍历集合_{}", 深度);
                let 索引名 = 格式化!("_遍历索引_{}", 深度);
                self.进入作用域();
                self.编译表达式(集合)?;
                let 集合槽位 = self.声明局部变量(&集合名);
                self.代码块.写入操作码(操作码::设置局部, self.当前行号);
                self.代码块.写入字节(集合槽位);
                let i = self.代码块.添加常量(常量::整数(0));
                self.代码块.写入操作码(操作码::压入常量, self.当前行号);
                self.代码块.写入u16(i);
                let 索引槽位 = self.声明局部变量(&索引名);
                self.代码块.写入操作码(操作码::设置局部, self.当前行号);
                self.代码块.写入字节(索引槽位);
                // 循环开始
                let 循环开始 = self.代码块.当前地址();
                self.循环栈.push(循环上下文 { break跳转列表: Vec::new(), continue跳转列表: Vec::new(), continue目标: 循环开始 });
                // 条件：_索引 < 长度(_集合)
                self.代码块.写入操作码(操作码::读取局部, self.当前行号);
                self.代码块.写入字节(索引槽位);
                let 长度索引 = self.获取全局索引("长度");
                self.代码块.写入操作码(操作码::读取全局, self.当前行号);
                self.代码块.写入u16(长度索引);
                self.代码块.写入操作码(操作码::读取局部, self.当前行号);
                self.代码块.写入字节(集合槽位);
                self.代码块.写入操作码(操作码::调用, self.当前行号);
                self.代码块.写入字节(1);
                self.代码块.写入操作码(操作码::小于, self.当前行号);
                let 跳转结束 = self.代码块.当前地址();
                self.代码块.写入操作码(操作码::假性跳转, self.当前行号);
                self.代码块.写入u16(0);
                // let 变量名 = _集合[_索引]
                self.代码块.写入操作码(操作码::读取局部, self.当前行号);
                self.代码块.写入字节(集合槽位);
                self.代码块.写入操作码(操作码::读取局部, self.当前行号);
                self.代码块.写入字节(索引槽位);
                self.代码块.写入操作码(操作码::索引读取, self.当前行号);
                let 变量槽位 = self.声明局部变量(变量名);
                self.代码块.写入操作码(操作码::设置局部, self.当前行号);
                self.代码块.写入字节(变量槽位);
                for s in 体 { self.编译语句(s)?; }
                // continue目标：索引递增
                let continue目标 = self.代码块.当前地址();
                self.代码块.写入操作码(操作码::读取局部, self.当前行号);
                self.代码块.写入字节(索引槽位);
                let one = self.代码块.添加常量(常量::整数(1));
                self.代码块.写入操作码(操作码::压入常量, self.当前行号);
                self.代码块.写入u16(one);
                self.代码块.写入操作码(操作码::加, self.当前行号);
                self.代码块.写入操作码(操作码::设置局部, self.当前行号);
                self.代码块.写入字节(索引槽位);
                // 跳回循环开始
                let 跳转地址 = self.代码块.当前地址();
                self.代码块.写入操作码(操作码::无条件跳转, self.当前行号);
                let 偏移 = (循环开始 as i64 - 跳转地址 as i64 - 3) as u16;
                self.代码块.写入u16(偏移);
                let 结束 = self.代码块.当前地址();
                self.代码块.回填跳转(跳转结束 + 1, 结束);
                let 循环上下文 = self.循环栈.pop().unwrap();
                for 地址 in 循环上下文.break跳转列表 { self.代码块.回填跳转(地址, 结束); }
                for 地址 in 循环上下文.continue跳转列表 { self.代码块.回填跳转(地址, continue目标); }
                self.退出作用域();
                self.遍历深度 -= 1;
            }
            语句::跳出 => {
                if let Some(上下文) = self.循环栈.last_mut() {
                    let 地址 = self.代码块.当前地址();
                    self.代码块.写入操作码(操作码::无条件跳转, self.当前行号);
                    上下文.break跳转列表.push(地址 + 1);
                    self.代码块.写入u16(0);
                }
            }
            语句::继续 => {
                if let Some(上下文) = self.循环栈.last_mut() {
                    let 地址 = self.代码块.当前地址();
                    self.代码块.写入操作码(操作码::无条件跳转, self.当前行号);
                    上下文.continue跳转列表.push(地址 + 1);
                    self.代码块.写入u16(0);
                }
            }
            语句::返回(表达式) => {
                if let Some(表达式) = 表达式 { self.编译表达式(表达式)?; } else { self.代码块.写入操作码(操作码::压入空, self.当前行号); }
                self.代码块.写入操作码(操作码::返回, self.当前行号);
            }
            语句::函数定义 { 名称, 参数, 体, 是否构造, .. } => {
                let 跳转到体后 = self.代码块.当前地址();
                self.代码块.写入操作码(操作码::无条件跳转, self.当前行号);
                self.代码块.写入u16(0);
                let 起始地址 = self.代码块.当前地址();
                self.进入作用域();
                if *是否构造 {
                    // 构造函数：槽位0存放实例对象
                    self.声明局部变量("_实例");
                } else {
                    self.声明局部变量("_函数"); // 槽位0留给函数对象
                }
                for 参数 in 参数 { self.声明局部变量(&参数.名称); }
                for s in 体 { self.编译语句(s)?; }
                if *是否构造 {
                    // 构造函数：返回本（实例）
                    self.代码块.写入操作码(操作码::读取局部, self.当前行号);
                    self.代码块.写入字节(0);
                } else {
                    self.代码块.写入操作码(操作码::压入空, self.当前行号);
                }
                self.代码块.写入操作码(操作码::返回, self.当前行号);
                let 局部数量 = self.当前作用域局部数量();
                self.退出作用域();
                let 函数结束 = self.代码块.当前地址();
                self.代码块.回填跳转(跳转到体后 + 1, 函数结束);
                let 函数索引 = self.代码块.添加常量(常量::函数(函数信息 { 名称: 名称.clone(), 参数数量: 参数.len() as u8, 局部变量数量: 局部数量, 起始地址 }));
                let 全局索引 = self.获取全局索引(名称);
                self.代码块.写入操作码(操作码::压入常量, self.当前行号);
                self.代码块.写入u16(函数索引);
                self.代码块.写入操作码(操作码::设置全局, self.当前行号);
                self.代码块.写入u16(全局索引);
            }
            语句::类定义 { 名称, 父类, 成员, .. } => {
                self.编译类定义(名称, 父类.as_deref(), 成员)?;
            }
            语句::抛出(表达式) => {
                self.编译表达式(表达式)?;
                // 抛出暂时用停止操作码实现，后续可扩展异常处理
                self.代码块.写入操作码(操作码::停止, self.当前行号);
            }
            语句::尝试捕获 { 体, 捕获变量, 捕获体 } => {
                // 简化实现：直接编译体，捕获部分暂不支持异常传播
                for s in 体 { self.编译语句(s)?; }
                let _ = 捕获变量;
                let _ = 捕获体;
            }
            语句::匹配 { 值, 分支 } => {
                self.编译匹配(值, 分支)?;
            }
            语句::块(语句列表) => { self.进入作用域(); for s in 语句列表 { self.编译语句(s)?; } self.退出作用域(); }
        }
        Ok(())
    }

    fn 编译表达式(&mut self, 表达式: &表达式) -> Result<(), 编译错误> {
        match 表达式 {
            表达式::整数(n) => { let i = self.代码块.添加常量(常量::整数(*n)); self.代码块.写入操作码(操作码::压入常量, self.当前行号); self.代码块.写入u16(i); }
            表达式::小数(n) => { let i = self.代码块.添加常量(常量::小数(*n)); self.代码块.写入操作码(操作码::压入常量, self.当前行号); self.代码块.写入u16(i); }
            表达式::文本(s) => { let i = self.代码块.添加常量(常量::文本(s.clone())); self.代码块.写入操作码(操作码::压入常量, self.当前行号); self.代码块.写入u16(i); }
            表达式::布尔(true) => { self.代码块.写入操作码(操作码::压入真, self.当前行号); }
            表达式::布尔(false) => { self.代码块.写入操作码(操作码::压入假, self.当前行号); }
            表达式::空值 => { self.代码块.写入操作码(操作码::压入空, self.当前行号); }
            表达式::标识符(名称) => {
                if let Some(槽位) = self.查找局部变量(名称) {
                    self.代码块.写入操作码(操作码::读取局部, self.当前行号);
                    self.代码块.写入字节(槽位);
                } else {
                    let 索引 = self.获取全局索引(名称);
                    self.代码块.写入操作码(操作码::读取全局, self.当前行号);
                    self.代码块.写入u16(索引);
                }
            }
            表达式::二元运算 { 左, 运算符, 右 } => {
                self.编译表达式(左)?;
                self.编译表达式(右)?;
                let 操作码 = match 运算符 {
                    运算符类型::加 => 操作码::加, 运算符类型::减 => 操作码::减,
                    运算符类型::乘 => 操作码::乘, 运算符类型::除 => 操作码::除,
                    运算符类型::取余 => 操作码::取余,
                    运算符类型::等于 => 操作码::相等, 运算符类型::不等于 => 操作码::不等,
                    运算符类型::小于 => 操作码::小于, 运算符类型::大于 => 操作码::大于,
                    运算符类型::小于等于 => 操作码::小于等于, 运算符类型::大于等于 => 操作码::大于等于,
                    运算符类型::逻辑与 => 操作码::逻辑与, 运算符类型::逻辑或 => 操作码::逻辑或,
                    _ => return Err(编译错误::未定义变量("不支持的运算符".into(), crate::错误::位置::新建("", 0, 0))),
                };
                self.代码块.写入操作码(操作码, self.当前行号);
            }
            表达式::一元运算 { 运算符, 操作数 } => {
                self.编译表达式(操作数)?;
                match 运算符 {
                    运算符类型::取反 => self.代码块.写入操作码(操作码::取反, self.当前行号),
                    运算符类型::逻辑非 => self.代码块.写入操作码(操作码::逻辑非, self.当前行号),
                    _ => {}
                }
            }
            表达式::函数调用 { 函数, 参数 } => {
                self.编译表达式(函数)?;
                for 参数 in 参数 { self.编译表达式(参数)?; }
                self.代码块.写入操作码(操作码::调用, self.当前行号);
                self.代码块.写入字节(参数.len() as u8);
            }
            表达式::成员访问 { 对象, 成员 } => {
                self.编译表达式(对象)?;
                let i = self.代码块.添加常量(常量::文本(成员.clone()));
                self.代码块.写入操作码(操作码::属性读取, self.当前行号);
                self.代码块.写入u16(i);
            }
            表达式::索引访问 { 对象, 索引 } => {
                self.编译表达式(对象)?;
                self.编译表达式(索引)?;
                self.代码块.写入操作码(操作码::索引读取, self.当前行号);
            }
            表达式::数组字面量(元素) => {
                for 元素 in 元素 { self.编译表达式(元素)?; }
                self.代码块.写入操作码(操作码::构建数组, self.当前行号);
                self.代码块.写入u16(元素.len() as u16);
            }
            表达式::映射字面量(对) => {
                for (键, 值) in 对 { self.编译表达式(键)?; self.编译表达式(值)?; }
                self.代码块.写入操作码(操作码::构建映射, self.当前行号);
                self.代码块.写入u16(对.len() as u16);
            }
            表达式::本引用 => {
                // 方法中本引用槽位1（实例），普通函数中槽位0
                let 槽位 = if self.在方法中 { 1 } else { 0 };
                self.代码块.写入操作码(操作码::读取局部, self.当前行号);
                self.代码块.写入字节(槽位);
            }
            表达式::箭头函数 { 参数, 体 } => {
                // 箭头函数编译为普通函数定义
                let 跳转到体后 = self.代码块.当前地址();
                self.代码块.写入操作码(操作码::无条件跳转, self.当前行号);
                self.代码块.写入u16(0);
                let 起始地址 = self.代码块.当前地址();
                self.进入作用域();
                self.声明局部变量("_函数");
                for 参数 in 参数 { self.声明局部变量(&参数.名称); }
                self.编译语句(体)?;
                self.代码块.写入操作码(操作码::压入空, self.当前行号);
                self.代码块.写入操作码(操作码::返回, self.当前行号);
                let 局部数量 = self.当前作用域局部数量();
                self.退出作用域();
                let 函数结束 = self.代码块.当前地址();
                self.代码块.回填跳转(跳转到体后 + 1, 函数结束);
                let 函数索引 = self.代码块.添加常量(常量::函数(函数信息 { 名称: "匿名函数".into(), 参数数量: 参数.len() as u8, 局部变量数量: 局部数量, 起始地址 }));
                self.代码块.写入操作码(操作码::压入常量, self.当前行号);
                self.代码块.写入u16(函数索引);
            }
        }
        Ok(())
    }

    fn 编译类定义(&mut self, 名称: &str, 父类: Option<&str>, 成员: &[类成员]) -> Result<(), 编译错误> {
        // 编译类的方法为独立函数，注册到全局
        let mut 方法映射: Vec<(String, u8, u8, usize)> = Vec::new(); // (名称, 参数数量, 局部数量, 起始地址)
        let mut 构造函数信息: Option<(u8, u8, usize)> = None; // (参数数量, 局部数量, 起始地址)

        for 成员 in 成员 {
            match 成员 {
                类成员::构造函数 { 参数, 体 } => {
                    let 跳转到体后 = self.代码块.当前地址();
                    self.代码块.写入操作码(操作码::无条件跳转, self.当前行号);
                    self.代码块.写入u16(0);
                    let 起始地址 = self.代码块.当前地址();
                    self.进入作用域();
                    self.声明局部变量("_实例"); // 槽位0存放实例
                    // 创建空实例并存入槽位0
                    let 类名索引 = self.代码块.添加常量(常量::文本(名称.to_string()));
                    self.代码块.写入操作码(操作码::构建实例, self.当前行号);
                    self.代码块.写入u16(类名索引);
                    self.代码块.写入操作码(操作码::设置局部, self.当前行号);
                    self.代码块.写入字节(0);
                    for 参数 in 参数 { self.声明局部变量(&参数.名称); }
                    for s in 体 { self.编译语句(s)?; }
                    // 返回实例
                    self.代码块.写入操作码(操作码::读取局部, self.当前行号);
                    self.代码块.写入字节(0);
                    self.代码块.写入操作码(操作码::返回, self.当前行号);
                    let 局部数量 = self.当前作用域局部数量();
                    self.退出作用域();
                    let 函数结束 = self.代码块.当前地址();
                    self.代码块.回填跳转(跳转到体后 + 1, 函数结束);
                    构造函数信息 = Some((参数.len() as u8, 局部数量, 起始地址));
                }
                类成员::方法 { 名称: 方法名, 参数, 体, .. } => {
                    let 跳转到体后 = self.代码块.当前地址();
                    self.代码块.写入操作码(操作码::无条件跳转, self.当前行号);
                    self.代码块.写入u16(0);
                    let 起始地址 = self.代码块.当前地址();
                    self.进入作用域();
                    // 槽位0是函数对象，槽位1是实例（本），参数从槽位2开始
                    self.声明局部变量("_函数"); // 槽位0
                    self.声明局部变量("_本");   // 槽位1 = 实例
                    for 参数 in 参数 { self.声明局部变量(&参数.名称); }
                    let 旧方法标志 = self.在方法中;
                    self.在方法中 = true;
                    for s in 体 { self.编译语句(s)?; }
                    self.在方法中 = 旧方法标志;
                    self.代码块.写入操作码(操作码::压入空, self.当前行号);
                    self.代码块.写入操作码(操作码::返回, self.当前行号);
                    let 局部数量 = self.当前作用域局部数量();
                    self.退出作用域();
                    let 函数结束 = self.代码块.当前地址();
                    self.代码块.回填跳转(跳转到体后 + 1, 函数结束);
                    方法映射.push((方法名.clone(), 参数.len() as u8, 局部数量, 起始地址));
                }
                类成员::属性 { .. } => { /* 属性在实例化时创建 */ }
            }
        }

        // 把构造函数和方法作为独立函数注册到全局
        let 全局索引 = self.获取全局索引(名称);

        // 如果有构造函数，将构造函数作为类的值注册
        if let Some((参数数量, 局部数量, 起始地址)) = 构造函数信息 {
            let 函数索引 = self.代码块.添加常量(常量::函数(函数信息 {
                名称: 名称.to_string(),
                参数数量,
                局部变量数量: 局部数量,
                起始地址,
            }));
            self.代码块.写入操作码(操作码::压入常量, self.当前行号);
            self.代码块.写入u16(函数索引);
        } else {
            // 没有构造函数，创建一个默认的空构造函数
            let 跳转到体后 = self.代码块.当前地址();
            self.代码块.写入操作码(操作码::无条件跳转, self.当前行号);
            self.代码块.写入u16(0);
            let 起始地址 = self.代码块.当前地址();
            self.代码块.写入操作码(操作码::压入空, self.当前行号);
            self.代码块.写入操作码(操作码::返回, self.当前行号);
            let 函数结束 = self.代码块.当前地址();
            self.代码块.回填跳转(跳转到体后 + 1, 函数结束);
            let 函数索引 = self.代码块.添加常量(常量::函数(函数信息 {
                名称: 名称.to_string(),
                参数数量: 0,
                局部变量数量: 1,
                起始地址,
            }));
            self.代码块.写入操作码(操作码::压入常量, self.当前行号);
            self.代码块.写入u16(函数索引);
        }
        self.代码块.写入操作码(操作码::设置全局, self.当前行号);
        self.代码块.写入u16(全局索引);

        // 注册方法为全局函数（格式：类名_方法名）
        for (方法名, 参数数量, 局部数量, 起始地址) in 方法映射 {
            let 全函数名 = 格式化!("{}_{}", 名称, 方法名);
            let 方法全局索引 = self.获取全局索引(&全函数名);
            let 函数索引 = self.代码块.添加常量(常量::函数(函数信息 {
                名称: 全函数名,
                参数数量,
                局部变量数量: 局部数量,
                起始地址,
            }));
            self.代码块.写入操作码(操作码::压入常量, self.当前行号);
            self.代码块.写入u16(函数索引);
            self.代码块.写入操作码(操作码::设置全局, self.当前行号);
            self.代码块.写入u16(方法全局索引);
        }

        Ok(())
    }

    fn 编译匹配(&mut self, 值: &表达式, 分支: &[匹配分支]) -> Result<(), 编译错误> {
        // 编译匹配语句为一系列 if-else
        // 暂时只支持字面量模式和通配符
        self.编译表达式(值)?;
        let 值槽位 = self.声明局部变量("_匹配值");
        self.代码块.写入操作码(操作码::设置局部, self.当前行号);
        self.代码块.写入字节(值槽位);

        let mut 跳转结束列表 = Vec::new();

        let 分支数量 = 分支.len();
        for (i, 当前分支) in 分支.iter().enumerate() {
            match &当前分支.模式 {
                匹配模式::字面量(表达式) => {
                    self.代码块.写入操作码(操作码::读取局部, self.当前行号);
                    self.代码块.写入字节(值槽位);
                    self.编译表达式(表达式)?;
                    self.代码块.写入操作码(操作码::相等, self.当前行号);
                    let 跳转假 = self.代码块.当前地址();
                    self.代码块.写入操作码(操作码::假性跳转, self.当前行号);
                    self.代码块.写入u16(0);
                    for s in &当前分支.体 { self.编译语句(s)?; }
                    // 跳到匹配结束
                    let 跳转结束 = self.代码块.当前地址();
                    self.代码块.写入操作码(操作码::无条件跳转, self.当前行号);
                    跳转结束列表.push(跳转结束 + 1);
                    self.代码块.写入u16(0);
                    let 假开始 = self.代码块.当前地址();
                    self.代码块.回填跳转(跳转假 + 1, 假开始);
                }
                匹配模式::标识符(名称) => {
                    // 绑定变量
                    self.代码块.写入操作码(操作码::读取局部, self.当前行号);
                    self.代码块.写入字节(值槽位);
                    let 绑定槽位 = self.声明局部变量(名称);
                    self.代码块.写入操作码(操作码::设置局部, self.当前行号);
                    self.代码块.写入字节(绑定槽位);
                    for s in &当前分支.体 { self.编译语句(s)?; }
                    if i < 分支数量 - 1 {
                        let 跳转结束 = self.代码块.当前地址();
                        self.代码块.写入操作码(操作码::无条件跳转, self.当前行号);
                        跳转结束列表.push(跳转结束 + 1);
                        self.代码块.写入u16(0);
                    }
                }
                匹配模式::通配符 => {
                    for s in &当前分支.体 { self.编译语句(s)?; }
                }
            }
        }

        let 结束 = self.代码块.当前地址();
        for 地址 in 跳转结束列表 { self.代码块.回填跳转(地址, 结束); }
        Ok(())
    }

    fn 进入作用域(&mut self) { self.当前深度 += 1; self.作用域栈.push(作用域 { 局部变量: Vec::new(), 深度: self.当前深度 }); }
    fn 退出作用域(&mut self) { let 范围 = self.作用域栈.pop().unwrap(); for _ in &范围.局部变量 { self.代码块.写入操作码(操作码::弹出, self.当前行号); } self.当前深度 -= 1; }
    fn 声明局部变量(&mut self, 名称: &str) -> u8 {
        let 范围 = self.作用域栈.last_mut().unwrap();
        for 变量 in &范围.局部变量 { if 变量.名称 == 名称 { return 变量.槽位; } }
        let 槽位 = 范围.局部变量.len() as u8;
        范围.局部变量.push(局部变量 { 名称: 名称.to_string(), 槽位 });
        槽位
    }
    fn 查找局部变量(&self, 名称: &str) -> Option<u8> { for 范围 in self.作用域栈.iter().rev() { for 变量 in &范围.局部变量 { if 变量.名称 == 名称 { return Some(变量.槽位); } } } None }
    fn 当前作用域局部数量(&self) -> u8 { self.作用域栈.last().unwrap().局部变量.len() as u8 }
    fn 获取全局索引(&mut self, 名称: &str) -> u16 { *self.全局名称.entry(名称.to_string()).or_insert_with(|| self.代码块.添加常量(常量::文本(名称.to_string()))) }
}
